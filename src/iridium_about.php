                <p>Система Iridium - это беспроводная телефонная сеть мобильной персональной связи, работающая на низкоорбитальных спутниках и разработанная для предоставления набора стандартных телефонных услуг - голосовая связь, передача факсимильных сообщений и данных. Она сделала революционный переворот в области связи для профессионалов бизнеса, путешественников, жителей, проживающих в районах с неразвитой или отсутствующей связью, служб спасения, а также других лиц, которым необходима многофункциональная и удобная спутниковая связь в виде спутникового телефона-трубки с единым глобальным номером. В отличие от наземных сетей связи, спутниковая система отслеживает местоположение телефона, обеспечивая, таким образом, прохождение сигнала до абонента вне зависимости от его местонахождения. Иными словами, вы можете позвонить абоненту системы Iridium, не зная, где именно он находится. Телефоны Iridium обеспечивают высококачественное соединение для голосовой связи и предполагают интерфейсное соединение с ноутбуками, электронными органайзерами и другим телекоммуникационным оборудованием. Относительно короткое расстояние до спутника уменьшило задержку сигнала и улучшило качество разговора.</p>
