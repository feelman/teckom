                <p>Пользовательский сегмент Иридиум - это спутниковые телефоны, судовые и авиационные терминалы и спутниковые пейджеры, посредством которых абонент получает доступ в сеть Иридиум и пользуется ее услугами. Спутниковые телефоны Иридиум внешне выглядят, как мобильный телефон больших размеров.</p>
                <p>Концепцию системы Iridium предложили инженеры компании Motorola - Рэй Леопольд, Кен Петерсон и Бэри Бертайгер. Они предвидели успех группировки из низкоорбитальных спутников. Исследования и разработки начались в 1987 году. Начав оказание услуг с 1-го ноября 1998, компания Iridium LLC (основанная в 1991 и инвестировавшая в проект около 7 миллиардов долларов) объявила 17 марта 2000 года о прекращении обслуживания абонентов. Однако 22 ноября 2000 года вновь образованная компания Iridium Satellite LLC, подписав контракт на обслуживание с Министерством Обороны США, приступила к переводу активов и передаче управления группировкой компании Boeing. С 12 декабря 2000 года обслуживание абонентов Пентагона и Правительства США возобновлено. Коммерческая эксплуатация системы возобновилась 28 марта 2001 года. Система Иридиум нашла поддержку инвесторов в лице 19-ти стратегических партнеров со всего мира, включая: группу компаний "AIG", корпорацию "Iridium Africa", корпорацию "Iridium SudAmerica", корпорацию "Iridium Middle East", ГКНПЦ им. Хруничева, корпорацию "Lockheed Martin", "Iridium Canada, Inc.", "Iridium China (Hong Kong) Ltd.", "Iridium India Telecom Limited", "Iridium Italia S.p.A.", компанию "Raytheon", "SK Telecom", холдинг "South Pacific Iridium", "Sprint Iridium, Inc.", "Thai Satellite Telecommunications Co., Ltd.", "Motorola, Inc.", "Nippon Iridium (Bermuda) Limited", "Vebacom Holdings, Inc.", "Pacific Asia Communications Ltd."</p>
                <p>Долгое время, после возобновления коммерческой эксплуатации системы в 2001 году, Iridium Satellite LLC ограничивалась работой на американском рынке, но с 2003 года вышел за его пределы. Сегодня количество абонентов Iridium во всем мире составляет более 300 тыс. человек и постоянно растет. В настоящий момент учитывая глобальное покрытие, проверенную временем надежность у системы практически нет конкурентов.</p>
